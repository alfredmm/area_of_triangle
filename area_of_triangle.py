# A program that calculates the area of a triangle
# From user input
# 1/2 * B * H

def area_of_triangle():
    base = int(input("Enter Base of The Triangle: "))
    height = int(input("Enter Height of The Triangle: "))
    area = 1/2 * base * height

    print("The Area=",area)

area_of_triangle()
    
    
